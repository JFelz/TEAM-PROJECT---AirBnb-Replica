import { clientCredentials } from '../utils/client';

const dbUrl = clientCredentials.databaseURL;

const getUserListings = (uid) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/listings.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const getAllListings = () => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/listings.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const deleteListing = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/listings/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const getSingleListing = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/listings/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const createListing = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/listings.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const updateListing = (payload) => new Promise((resolve, reject) => {
  fetch(`${dbUrl}/listings/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

export {
  getUserListings,
  getSingleListing,
  getAllListings,
  deleteListing,
  updateListing,
  createListing,
};
